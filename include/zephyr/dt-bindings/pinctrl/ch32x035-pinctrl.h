#define CH32X035_PINMUX_PORT_PA 0
#define CH32X035_PINMUX_PORT_PB 1
#define CH32X035_PINMUX_PORT_PC 2

/* Starting bit for the remap field in PCFR1 */
#define CH32X035_PINMUX_SPI1_RM    0
#define CH32X035_PINMUX_I2C1_RM    2
#define CH32X035_PINMUX_USART1_RM  5
#define CH32X035_PINMUX_USART2_RM  7
#define CH32X035_PINMUX_USART3_RM  10
#define CH32X035_PINMUX_USART4_RM  12
#define CH32X035_PINMUX_TIM1_RM    15
#define CH32X035_PINMUX_TIM2_RM    18
#define CH32X035_PINMUX_TIM3_RM    21
#define CH32X035_PINMUX_PIOC_RM    23
#define CH32X035_PINMUX_SDI_RM     24

/* Port number with 0-2 */
#define CH32X035_PINCTRL_PORT_SHIFT    0
#define CH32X035_PINCTRL_PORT_MASK     GENMASK(1, 0)
/* Pin number 0-23 */
#define CH32X035_PINCTRL_PIN_SHIFT     2
#define CH32X035_PINCTRL_PIN_MASK      GENMASK(6, 2)
/* Base remap bit 0-31 */
#define CH32X035_PINCTRL_RM_BASE_SHIFT 7
#define CH32X035_PINCTRL_RM_BASE_MASK  GENMASK(11, 7)
/* Function remapping ID 0-7 */
#define CH32X035_PINCTRL_RM_SHIFT      12
#define CH32X035_PINCTRL_RM_MASK       GENMASK(14, 12)

#define CH32X035_PINMUX_DEFINE(port, pin, rm, remapping)                        \
	((CH32X035_PINMUX_PORT_##port << CH32X035_PINCTRL_PORT_SHIFT) |         \
	 (pin << CH32X035_PINCTRL_PIN_SHIFT) |                                  \
	 (CH32X035_PINMUX_##rm##_RM << CH32X035_PINCTRL_RM_BASE_SHIFT) |        \
	 (remapping << CH32X035_PINCTRL_RM_SHIFT))

